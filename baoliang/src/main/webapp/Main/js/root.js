function rootselectFunc(index)
{
	switch(index)
	{
		case 1:
			document.getElementById("frameK").src="Main/Rootmanagement.jsp";
			break;
		case 2:
			document.getElementById("frameK").src="Main/applicationmanager.jsp";
			break;
		case 3:
			document.getElementById("frameK").src="Main/Bindapanddriver.jsp";
			break;
		case 4:
			document.getElementById("frameK").src="DriverManager/DriverMa.jsp";
			break;
		case 5:
			document.getElementById("frameK").src="Main/shenhe.jsp";
			break;
	}
}